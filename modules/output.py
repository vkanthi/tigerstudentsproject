def output(nam, inp, re):

    """This is the function will prompt the user that the given word is a palindrome or not"""
    if (inp == re):
        return (f"Dear {nam}, the input word {inp} you have given is a palindrome")
    else:
        return (f"Dear {nam}, the input word {inp} you have given is not a palindrome")


