# Getting user name from the user 


def user_name():
    """Getting name from the user"""
    name = str(input("Enter your name:- ")).capitalize()
    return name


# Getting input for the palindrome function from the user


def user_input():
    """Getting user input for the palindrome function"""
    user_word = str(input("Enter a input you want the palindrome function to be performed:- ")).lower()
    return user_word


